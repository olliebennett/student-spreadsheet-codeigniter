<h3>What is Student Spreadsheet?</h3>

<ul>
	<li>a free, easy-to-use online expenses manager.</li>
	<li>removes the hassle of remembering who owes what!</li> When you have the means to pay for a large bill, for instance, you can relax for the rest of the year, knowing that your housemates can pay other expenses throughout the year. This is in contrast to having to chase each housemate up for their specific share of the bill, only to have them chase you later for your share of something for which they paid.
</ul>

<p>Using Student Spreadsheet is as simple as...</p>
<ol>
	<li>Record payments such as utility bills, including the price and which friends should contribute.</li>
	<li>Monitor how much you owe (or are owed) in total.</li>
	<li>Automatically calculate out who owes what, and settle-up in one payment at the end!</li>
</ol>

<p>A simplified example:<p>
<ul>
	<li>Three mates - Alice, Bruce and Carly - live together in shared accommodation.</li>
	<li>Bruce pays a £60 electricity bill.</li>
	<li>Alice buys the TV license for £120.</li>
	<li>Carly pays £30/month for internet.</li>
	<li>3 months later, they move out. Student Spreadsheet tells them the quickest way to repay:
		<ul>
			<li>Bruce pays Alice £30.</li>
		</ul>
	</li>
	<li>All housemates are now even!</li>
</ul>

